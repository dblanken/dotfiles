local updated_capabilities = vim.lsp.protocol.make_client_capabilities()

local cmp_loaded, cmp_nvim_lsp = pcall(require, 'cmp_nvim_lsp')
if cmp_loaded then
    updated_capabilities = cmp_nvim_lsp.update_capabilities(updated_capabilities)
